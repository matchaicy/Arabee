package com.example.arabee.data.model;

public class Hijaiyah {
     String ID;
     String image;

     public Hijaiyah(String ID, String image){
          this.ID = ID;
          this.image = image;
     }


     public String getID() {
          return ID;
     }

     public void setID(String ID) {
          this.ID = ID;
     }

     public String getImage() {
          return image;
     }

     public void setImage(String image) {
          this.image = image;
     }

}